    <h1>Espace FAQ</h1>

    <p>Clique <a href="https://www.efp.be/projets/plateforme-dora.html">ICI</a> pour basculer vers la plateforme DORA!</p>

    <h4>1. Comment puis-je trouver un stage qui correspond à ma formation ?</h4>
    <p>Vous pouvez consulter le Catalogue des Annonces de Stages, qui propose un tri par métier. Vous pouvez également contacter les Experts Métiers pour obtenir des conseils et des recommandations.</p>
    
    <h4>2. Puis-je trouver un stage en télétravail ou hybride ?</h4>
    <p>Oui, certaines entreprises proposent des stages à distance ou hybrides. Lors de votre recherche, utilisez les filtres pour sélectionner le type de stage souhaité (présentiel, hybride, à distance).</p>

    <h4>3. Comment puis-je postuler à un stage ?</h4>
    <p>Une fois que vous avez trouvé une annonce intéressante, vous pouvez postuler directement via la plateforme en envoyant votre CV et une lettre de motivation. Un formulaire de contact est aussi disponible pour joindre votre CV et demander un rendez-vous avec l’entreprise.</p>

    <h4>4. Quels sont les critères pour obtenir un stage rémunéré ?</h4>
    <p>Les stages rémunérés sont souvent proposés par les grandes entreprises ou pour les formations très demandées. Vous pouvez consulter la fiche explicative sur les stages rémunérés et non rémunérés pour en savoir plus sur les conditions.
    </p>

    <h4>5. Comment faire si je ne trouve pas de stage près de chez moi ?</h4>
    <p>Si vous avez des contraintes de transport, utilisez les filtres pour chercher un stage proche de votre domicile ou accessible en transports en commun. Vous pouvez aussi consulter la boîte à outils pour trouver des astuces et aides à la mobilité.</p>

    <h4>6. Comment puis-je me démarquer auprès des recruteurs ?</h4>
    <p>Vous pouvez créer un CV Vidéo via JobiBox, un outil qui permet de présenter vos compétences et votre motivation de manière dynamique.</p>

    <h4>7. Où puis-je trouver des ressources pour préparer ma candidature ?</h4>
    <p>La boîte à outils met à disposition des articles, des guides et des liens vers les ateliers D.O.R.A, qui vous aident à rédiger un CV, une lettre de motivation et à réussir un entretien.</p>

    <h4>8. À qui puis-je m’adresser en cas de difficulté pour trouver un stage ?</h4>
    <p>Si vous rencontrez des difficultés, vous pouvez contacter les <b>Experts Métiers</b> ou utiliser la plateforme <b>D.O.R.A</b>, qui propose un accompagnement et des conseils personnalisés pour les étudiants en recherche de stage.</p>
