<h2>La plateforme DORA</h2>

<p>L'acronyme D.O.R.A. signifie: Développement-Orientation-Réussite-Accrochage.  

    L'équipe du pôle est composée d'une dizaine de collaborateurs, experts, formés dans le domaine psycho-social, dont la mission est d'accompagner, de soutenir et d'outiller les apprenants de l'efp tout au long de leur parcours de formation. 
    
    Les objectifs du pôle D.O.R.A. sont de favoriser la réussite et l'accrochage de tous les apprenants.</p>

